# Méthode add
def add(a, b):
    return a + b


def sub(a, b):
    """

    :param a:
    :param b:
    :return:
    """
    return a - b


def carre(a):
    return a ** 2


def div(a, b):
    """

    :param a:
    :param b:
    :return:
    """
    return a / b


def multiplication(a, b):
    return a * b


